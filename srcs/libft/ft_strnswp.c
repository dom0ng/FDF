#include "libft.h"

void	ft_strnswp(char *s1, char *s2, int n)
{
	char	c;
	int		i;

	i = 0;
	while (s1[i] && s2[i] && i < n)
	{
		c = s1[i];
		s1[i] = s2[i];
		s2[i] = c;
		i++;
	}
}
